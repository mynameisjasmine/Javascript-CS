/ Problem Solving */

// 1. Understand the Problem  

// Questions to ask yourself when trying to understand a problem

/* 
1. Can I restate the problem in my own words?
2. What are the inputs that go into the problem?
3. What are the outputs that should come from the solution to the problem?
4. Can the outputs be determined from the input? (ie do I have enough information to solve the problem)
5. How should I label the important pieces of data that are part of the problem?
*/


// 2. Explore Concrete Examples

// Coming up with examples can help you understand the problem better

/* 
1. Start with simple examples
2. Progress to more complex examples
3. Explore examples with empty inputs
4. Explore examples with invalid inputs
*/


// 3. Break it Down

// Take the actual steps of the problem and write them down





// 4. Solve/ Simplify

// Solve the problem if you can...if you can't,   solve a simpler problem

/* 
1. Find the core difficulty in what you're trying to do
2. Temporarily ignore that difficulty
3. Write a simplified solution
4. Then incorporate that difficulty back in


*/




// 5. Look Back and Refactor

//  Even if your solution already works, it's always good to go back to your code and look for ways to make it better

/*
1. Can you check the result?
2. Can you derive the result differently?
3. Can you understand it at a glance?
4. Can you use the result or method for some other problem?
5. Can you improve the performance of your solution?
6. Can you think of other ways to refactor?
7. How have other people solved this problem?    
*/

